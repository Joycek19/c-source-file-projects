//variables and data types
//preprocessor directive
#include<stdio.h>
int main(){

    char a;
    char name;
    int age;
    float area;
    double savings;

     printf("Enter a character: ");
     scanf("%c",&a);
     printf("The character is %c\n",a);

     printf("Enter a name: ");
     scanf("%s",&name);
     printf("The name is %s\n",name);

     printf("Enter age: ");
     scanf("%d",&age);
     printf("The age is years %d\n",age);

     printf("Enter area: ");
     scanf("%f",&area);
     printf("The area is %f\n",area);

     printf("Enter the savings: ");
     scanf("%lf",&savings);
     printf("The savings are %lf\n",savings);

return 0;

}



























